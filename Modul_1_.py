# ПЕР = []     -    СПИСОК (изменяемый объект). Он поддерживает команду индексации.                                                              *
# ПЕР [::]     -    Вывод определенных данных со списка                                                     *
# ПЕР = ([], ) -    КОРТЕЖ, неизменяемый список-объект (то, что находится в [] скобках - изменяемая часть)  *
                         #КОРТЕЖ поддерживает конкатенацию и умножение
# ПЕР [ч][ч]=  -    Редактирование КОРТЕЖА (редактируется то, что находится в [] скобках)                   *
# СТР1 + СТР2  -    КОНКАТЕНАЦИЯ (сложение текстовых данных )                                               *
# ПЕР.append() -    Добавление нового элемента в конец СПИСКА                                               *
# ПЕР.extend() -    Добавление нового СПИСКА в уже текущий список                                           *
# ПЕР [ч] = СТР-    Команда замены элемента строки в СПИСКЕ на новый                                        *
# ПЕР.pop()    -    Удаление элемента из СЛОВАРЯ с последующим его сохранением и использованием
                            # данного элемента (в скобках записывается индекс)


#************************************************************************************************************
# ПЕР.remove() -    Команда удаления элемента из текущего списка                                            *
# ПЕР.replace()-    Команда, выполняющая замену элемента в текстовых типах данных                           *
                         # (В скобках вводим: "что заменить", "На что заменить)"                            *
# ПЕР.upper()  -    Команда преобразования текста в верхний регистр                                         *
# ПЕР.lower()  -    Команда преобразования текста в нижний регистр                                          *



#************************************************************************************************************
# ПЕР = { : }       -    СЛОВАРЬ, он состоит из ключа (пишется в {} скобках до знака :)                     *
                            # и значения (пиштся в {} скобках после знака :)                                *
                            # Ключом может быть только не изменяемый объект                                 *
# ПЕР [ключ]        -    Вывод на консоль значения данного ключа из СЛОВАРЯ                                                            *
# ПЕР [ключ] = знач -    Замена данных из СЛОВАРЯ или же добавление новых данных в словарь
# del ПЕР [ключ]    -    Удаление данных из СЛОВАРЯ
# ПЕР.update({})    -    Добавление нового словаря в уже существующий словарь
# ПЕР.get(ключ)     -    Вывод на консоль значения данного ключа из СЛОВАРЯ (если этого элемента нет
                            # в СЛОВАРЕ, то он выдаст в консоле: None). Если ввести в данной
                            # команде после ключа запятую и любой тип данных, то этот тип
                            # данных появится на консоле заместо None.
# ПЕР.pop(ключ)     -    Удаление элемента из СЛОВАРЯ с последующим его сохранением и использованием
                            # данного элемента
# ПЕР.keys()        -    Вывод на консоль из СЛОВАРЯ всех ключей данных
# ПЕР.values()      -    Вывод на консоль из СЛОВАРЯ всех значений данных
# ПЕР.items()       -    Вывод на консоль из СЛОВАРЯ всех ключей и их значений



#************************************************************************************************************
