                             # выделить на консоль конкретный элемент из МНОЖЕСТВО.
# set(СПИСОК)       -    Команда преобразования СПИСКА во МНОЖЕСТВО
# ПЕР.discard(э)    -    Команда удаления из МНОЖЕСТВО конкретного элемента данных. Отличается от
                             # команды "ПЕР.remove()" тем, что он не будет выдавать ошибку, если
                             # элемент не был найден во МНОЖЕСТВО
# ПЕР.add(э)        -    Команда добавления конкретного элемента данных во МНОЖЕСТВО.





